package mm;

public class WordVO {
	private String word;
	private int no;
	private String answer;
	
	
	
	public WordVO(String word, int no, String answer) {		
		this.word = word;
		this.no = no;
		this.answer = answer;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	
	
	
	
	

}
